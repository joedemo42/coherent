#ifndef	__SYS___PID_H__
#define	__SYS___PID_H__

/*
 * This internal header file is intended as the sole point of definition for
 * the internal data type "__pid_t", exactly equivalent to the POSIX.1 data
 * type "pid_t" but given an internal name so that header files may refer to
 * the type without exporting the user-level type unnecessarily.
 */

typedef	long		__pid_t;

#endif	/* ! defined (__SYS___PID_H__) */
